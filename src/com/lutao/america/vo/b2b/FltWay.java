package com.lutao.america.vo.b2b;

import java.util.Date;
import java.util.List;

public class FltWay {
	private String FromCode;

	private String ToCode;

	private int Way;

	private Date FltTime;

	private Date ArrTime;

	private List<Flt> Flts;

	public void setFromCode(String FromCode){
	this.FromCode = FromCode;
	}
	public String getFromCode(){
	return this.FromCode;
	}
	public void setToCode(String ToCode){
	this.ToCode = ToCode;
	}
	public String getToCode(){
	return this.ToCode;
	}
	public void setWay(int Way){
	this.Way = Way;
	}
	public int getWay(){
	return this.Way;
	}
	public void setFltTime(Date FltTime){
	this.FltTime = FltTime;
	}
	public Date getFltTime(){
	return this.FltTime;
	}
	public void setArrTime(Date ArrTime){
	this.ArrTime = ArrTime;
	}
	public Date getArrTime(){
	return this.ArrTime;
	}
	public void setFlts(List<Flt> Flts){
	this.Flts = Flts;
	}
	public List<Flt> getFlts(){
	return this.Flts;
	}
}
